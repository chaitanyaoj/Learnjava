package com.ict.learning.Learnjava;

public class UnderstandingForLoop {

	public static void main(String[] args) {
		for(int x=1;x<=10;x+=2) {
			System.out.println("The value of x is " + x);
			//Blank line
			System.out.println("");
			
		}
		
		int x= 1;
		while (x<=10);{
			System.out.println("The value of x is " + x);
			x+=2;
			
		}
		
		}

}
